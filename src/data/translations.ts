// Helper function to translate standard Bible Book names on the fly
export const getTranslatedBookName = (pName: string, lang: "pt" | "en"): string => {
  const norm = pName.toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, "");
  const entry = bookTranslations[norm];
  if (entry) {
    return lang === "en" ? entry.en : entry.pt;
  }
  return pName;
};

